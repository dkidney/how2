test = function(n){
    
    for(i in 1:n){
        rnorm(10)
        rpois(100,10)
    }
    
}

## *****************************************************************************
##                                     Rprof
## *****************************************************************************

Rprof(file = "Rprof.out", line.profiling = TRUE, interval = 0.02)

test(1000)
    
Rprof(append = TRUE)

summaryRprof("Rprof.out")

